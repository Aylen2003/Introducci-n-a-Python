def palindromos(lista_palabras):
    palindromo_lista = []
    for palabra in lista_palabras:
        palabra = palabra.lower()
        if palabra == palabra[::-1]:
            palindromo_lista.append(palabra)
    return palindromo_lista

lista_palabras = ["Anilina", "Reconocer", "Salas", "Oso", "Hola", "Python"]
resultado = palindromos(lista_palabras)
print("Pal√≠ndromos en la lista:", resultado)